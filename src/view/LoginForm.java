    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String username = edtUsername.getText();
        String password = new String(edtPassword.getPassword());
        if (username.equals("")) {
            JOptionPane.showMessageDialog(this, "Username tidak boleh kosong");
        } else if (password.equals("")) {
            JOptionPane.showMessageDialog(this, "Password tidak boleh kosong");
        }
        if (validateUser(username, password)) {
            JOptionPane.showMessageDialog(this, "Login Berhasil!, Selamat datang " + user.getUsername() + "!");
            Menu menu = new Menu();
            menu.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Invalid username or password");
        }
    }                                        

    private boolean validateUser(String username, String password) {
        String query = "SELECT * FROM user WHERE username = ? AND password = ?";
        try {
            DatabaseHelper databaseHelper = new DatabaseHelper();
            PreparedStatement statement = databaseHelper.getConnection().prepareStatement(query);
            statement.setString(1, username);
            statement.setString(2, password);
            ResultSet resultSet = statement.executeQuery();
            if (resultSet.next()) {
                user = new User(username, password);
                return true;
            }
        } catch (SQLException e) {
            System.err.println("Failed to validate login: " + e.getMessage());
        }

        return false;
    }

